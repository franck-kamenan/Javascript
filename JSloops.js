// While loop
var i = 11;
while(i <= 15) {

    document.writeln(i + "<br/>");
    i++;
}
document.writeln("<br/>");

// Do while loop
var j = 21;
do {

    document.writeln(j + "<br/>");
    j++;
} while(j <= 25);
document.writeln("<br/>");

// For loop
for(k = 1; k <= 5; k++) {

    document.writeln(k + "<br/>");
}
document.writeln("<br/>");

